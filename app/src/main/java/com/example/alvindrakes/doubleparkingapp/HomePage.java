        // for scanNumberPlateBtn
        Button scanPlateNumberBtn = (Button) findViewById(R.id.scanPlateNumber);

        scanPlateNumberBtn.setOnClickListener(new View.OnClickListener() {
            @Override
            public void onClick(View v) {
                Intent ocrIntent = new Intent(HomePage.this, OCR_reader.class);
                startActivityForResult(ocrIntent, RC_OCR_CAPTURE);
            }
        });
